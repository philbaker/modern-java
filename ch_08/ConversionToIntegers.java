public class ConversionToIntegers {
    public static void main(String[] args) {
        int valueOfA = 'a';

        System.out.println(valueOfA);

        char gee = 'g';

        // All the letters from a to z have numeric values
        boolean isLetter = gee >= 'a' && gee <= 'z';

        System.out.println(isLetter);
    }
}
