class Muppet3 {
    String name;
}

class Muppet4 {
    boolean talented = true;
}

void main() {
    Muppet3 kermit = new Muppet3();
    kermit.name = "Kermit the Frog";
    System.out.println(kermit.name);
    // Kermit the Frog

    Muppet4 kermit2 = new Muppet4();
    System.out.println(kermit2.talented);
    // true
}

