class Muppet5 {
    int age;
    double salary;
    boolean talented;
    String name;
}

void main() {
    Muppet5 kermit3 = new Muppet5();

    System.out.println(kermit3.age);
    // 0

    System.out.println(kermit3.salary);
    // 0.0

    System.out.println(kermit3.talented);
    // false

    System.out.println(kermit3.name);
    // null
}
