public class Subtraction {
    public static void main(String[] args) {
        int x = 5;
        int y = x - 1;
        int z = x - y;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

        int w = x - -4;

        System.out.println(x);
        System.out.println(w);
    }
}
