public class Multiplication {
    public static void main(String[] args) {
        double x = 3;
        double y = x * 9;
        double z = y * 0.5;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

        double a = 1.5 * 2;
        System.out.println(a);
    }
}
