public class ReassignmentShorthands {
    public static void main(String[] args) {
        double x = 0.5;
        System.out.println(x);

        x += 3;
        System.out.println(x);

        x -=1;
        System.out.println(x);

        x++;
        System.out.println(x);

        x--;
        System.out.println(x);

        x *= 5;
        System.out.println(x);

        x /= 2;
        System.out.println(x);
    }
}
